// Increment (++) or Decrement (--)

let a = 1;
a++; //a=a+1
console.log(a);

let b = 5;
b--; //b=b-1;
console.log(b);

/*
PostFix Increment   a++
Prefix Increment   ++a
 */

let x = 3;
const y = x++; //3

console.log("X : ", x, "Y : ", y);

let i = 3;
const j = ++i;
console.log("I : ", i, "J : ", j);
